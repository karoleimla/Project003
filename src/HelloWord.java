public class HelloWord {                      // class on methodite kogum (nimega HelloWord)
    public static void main(String[] args) {  // method on avalduste/statements jada (nimega main)
        System.out.println("Hello, World!");  // statement on koodi rida, mis teeb lihtsat operatsiooni
        System.out.println("How are you?");   // another statement
        System.out.print("Goodbye, ");
        System.out.println("cruel world");
        System.out.print("Hello!\nHow are you doing?\n");   // lisab uue rea (newline)
        System.out.println("She said \"Hello!\" to me.");   // double quote
        System.out.println("tab \ttab \ttab \ttab");   // double quote

    }
}
